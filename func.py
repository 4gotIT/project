def func_addition(a, b):
    if (isinstance(a, int) or isinstance(a, float)) and (isinstance(b, int) or isinstance(b, float)):
        return a + b
    else:
        return 'Ошибка'


def func_subtraction(a, b):
    if (isinstance(a, int) or isinstance(a, float)) and (isinstance(b, int) or isinstance(b, float)):
        return a + b
    else:
        return 'Ошибка'
